def return_camp_id(camp_name):
    if camp_name.lower() == 'sotp':
        return 1
    elif camp_name.lower() == 'gmcs':
        return 2
    elif camp_name.lower() == 'slsip':
        return 3
    elif camp_name.lower() == 'kshitij':
        return 4
    elif camp_name.lower() == 'ugetin':
        return 5
    elif camp_name.lower() == 'sort':
        return 6
    elif camp_name.lower() == 'sppt':
        return 7
    elif camp_name.lower() == 'iprs':
        return 8
    else:
        return 1